	CREATE TABLE EQUIPO(
		
		ID				BIGINT			NOT NULL,
		NOMBRE			VARCHAR(100)		 	,
		
		PRIMARY KEY(ID)
		
		);
		
	CREATE TABLE PARTIDO(
	
		ID				BIGINT 			NOT NULL,
		ID_LOCAL		BIGINT			NOT NULL,
		ID_VISITANTE 	BIGINT			NOT NULL,
		ESTADO		 	VARCHAR					,
		GOLES_LOCALES 	INTEGER					,
		GOLES_VISITANTE INTEGER					,
		
		PRIMARY KEY(ID),
		
		FOREIGN KEY(ID_LOCAL) REFERENCES EQUIPO(ID),
		FOREIGN KEY(ID_VISITANTE) REFERENCES EQUIPO(ID)
	);
